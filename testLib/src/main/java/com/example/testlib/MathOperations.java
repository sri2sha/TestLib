package com.example.testlib;

import android.content.Context;

public class MathOperations {
    double addition(Context c,double num1, double num2){
        return num1+num2;
    }
    double subtraction(Context c,double num1, double num2){
        return num1-num2;
    }
    double multiplication(Context c,double num1, double num2){
        return num1*num2;
    }
    double division(Context c,double num1, double num2){
        return num1/num2;
    }
}
